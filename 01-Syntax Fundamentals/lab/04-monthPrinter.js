// 4 month printer
function month(date) {
    if(date === 1){
        console.log("January");
    }else if (date === 2){
        console.log("February");
    }else if (date === 3){
        console.log("March");
    }else if (date === 4){
        console.log("April");
    }else if (date === 5){
        console.log("May");
    }else if (date === 6){
        console.log("June");
    }else if (date === 7){
        console.log("July");
    }else if (date === 8){
        console.log("August");
    }else if (date === 9){
        console.log("September");
    }else if (date === 10){
        console.log("October");
    }else if (date === 11){
        console.log("November");
    }else if (date === 12){
        console.log("December");
    }else{
        console.log("Error!");
    }
}

month(13);